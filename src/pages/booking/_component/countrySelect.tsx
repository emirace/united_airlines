const CountrySelect: React.FC<{
  passenger: IPassenger;
  id: string;
  handleChange: (id: number, field: string, value: string) => void;
}> = ({ passenger, handleChange, id }) => {
  const [search, setSearch] = useState("");
  const [showDropdown, setShowDropdown] = useState(false);

  // Filter countries based on search input
  const filteredCountries = countries.filter((country) =>
    country.toLowerCase().includes(search.toLowerCase())
  );

  // Handle country selection
  const handleSelect = (selectedCountry: string) => {
    handleChange(passenger.id, id, selectedCountry);
    setSearch(selectedCountry);
    setShowDropdown(false);
  };

  return (
    <div className="relative w-full">
      <input
        type="text"
        value={search}
        onChange={(e) => setSearch(e.target.value)}
        onFocus={() => setShowDropdown(true)}
        className="w-full p-2 border rounded-md"
        placeholder="Search"
      />

      {showDropdown && filteredCountries.length > 0 && (
        <ul className="absolute z-10 w-full mt-1 bg-white border border-gray-300 rounded-md shadow-md max-h-48 overflow-y-auto">
          {filteredCountries.map((country) => (
            <li
              key={country}
              onClick={() => handleSelect(country)}
              className="px-4 py-2 hover:bg-gray-100 cursor-pointer"
            >
              {country}
            </li>
          ))}
        </ul>
      )}
    </div>
  );
};

export default CountrySelect;
